package com.firefly.core.users.interfaces.enums;

/**
 * Enum representing the theme preferences available to users.
 * Based on the theme_preference_enum in the database.
 */
public enum ThemePreferenceEnum {
    /**
     * Represents the light theme.
     */
    LIGHT,
    
    /**
     * Represents the dark theme.
     */
    DARK,
    
    /**
     * Represents the system default theme.
     */
    SYSTEM
}